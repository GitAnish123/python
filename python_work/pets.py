# You can use the "remove()" method to remove any specific instances in a list.
# Lets do the value "cat" for an example,
""" for example: """

pets = ['dog', 'cat', 'dog', 'goldfish', 'cat', 'rabbit', 'cat']
print(pets)

while 'cat' in pets:
    pets.remove('cat')

print(pets)


